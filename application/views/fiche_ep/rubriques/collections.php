<div class="rubrique-description">
  <p>Les Réserves Naturelles ont acquis une notoriété due à leur expérience de conservation du patrimoine géologique sur le terrain, base de la géologie ;
     mais aussi de mise en valeur de sites pour l’accueil  des publics.<br />
Il ne faut pas oublier que le patrimoine géologique est aussi constitué d’échantillons extraits des RN, ou de sites faisant partie du périmètre de la Réserve,
 prélevés avant le classement de celle-ci, ainsi que d’autres éléments (documents, ouvrages, autres collections…).<br />
Les situations sont assez contrastées, en fonction des gestionnaires.</p>

<h4>Collections ou chantillons</h4>
<p>Le gestionnaire peut donc gérer des collections ou des échantillons issus de la Réserve, issues du périmètre du décret initial ou du périmètre de protection.
  Des échantillons issus des sites de la Réserve peuvent aussi se trouver dans d’autres collections publiques (muséums, universités, autres musées).<br />
Les statuts du gestionnaire peuvent prévoir cette activité de conservation, qui peut être identifiée comme un des objectifs du plan de gestion.</p>

<h4>Gestion des collections</h4>
<p>Les inventaires sont généralement en cours de réalisation, les gestionnaires ayant
prioritairement travaillé sur d’autres missions de gestion des Réserves.<br />
Pour certains, se pose la question de la labellisation des collections (musée de France),
vu que le statut du gestionnaire en fait généralement une collection privée. <br />
Parmi les échantillons, les types et les figurés ont un statut particulier, et doivent
être répertoriés dans le cadre de programmes spécifiques ;
c’est le cas du <a href="http://transtyfipal.u-bourgogne.fr/" target="_blank">programme TransTyFiPal</a> pour la paléontologie,
programme géré par l’Université de Dijon.</p>


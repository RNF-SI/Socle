    <p>Les littoraux rocheux se développent là où les assauts de la mer se confrontent
directement avec les terres émergées. On peut y retrouver la plupart des
morphologies caractéristiques des aires continentales. Il existe cependant des
formes très spécifiques au contexte côtier, les unes liées à la remontée récente
du niveau marin dans d’anciennes vallées, ria, aber ou calanque, les autres liées
à la violence des assauts de tempêtes et au recul de la côte, platiers, falaises
vives, etc. Le découpage des roches par l’activité de la mer est aussi l’occasion
de mettre en évidence de belles structures, telles que des plis, des failles, des
chaos, des stratifications, etc.<br />
Il est donc nécessaire, pour décrire ce type de territoire, de passer en revue l’ensemble
des morphologies citées dans les rubriques précédentes.</p>

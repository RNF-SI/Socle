    <p>La plus grande plage d’Europe constitue le littoral landais sur 200 kilomètre de
long, entre la pointe de la Grave et la Chambre d’Amour au sud de l’embouchure
de l’Adour, au Pays basque. Les sables issus de l’érosion de roches dures riches
en quartz telles que les granites, sont transportés par les rivières et les fleuves
jusqu’à leur embouchure. Ils sont alors en partie repris par les courants de dérive
littorale qui les étalent le long des côtes en plages. Les matériaux plus fins, tels
que les particules argileuses sont, dans leur presque totalité, évacués au large, en
pleine mer. Cependant certains se déposent sur le littoral, sous forme de vases
souvent à l’origine de milieux particuliers.<br />
Quant aux galets, ils naissent pour la plupart de la fracturation <i>in situ</i> des roches
en place. Ceux du littoral du Pays de Caux proviennent des silex de la craie, libérés
de la roche lors de sa dissolution.</p>

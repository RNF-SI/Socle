    <p>Le vent qui vient du large souffle avec vigueur sur les côtes. Sur les plages de
grande largeur, le sable est sec en partie haute. Les grains n’y sont pas collés par
l’eau et le vent les déplace en direction de la terre. Les uns après les autres, ils se
mettent en mouvement par petits bonds successifs. D’autres s’envolent quand
le vent souffle en rafale. En arrière de la plage, le vent rencontre des reliefs et
de la végétation qui lui barrent le chemin. Il faiblit et son pouvoir de transport
diminue. Les grains s’arrêtent, s’accumulent et forment un tas : ainsi naissent les
dunes littorales. La croissance d’une grande dune nécessite des vents forts et
réguliers, ainsi qu’une importante réserve d’alimentation en sable.</p>

<p>Une dune s’édifie perpendiculairement à la direction du vent et présente un
profil transversal dissymétrique avec une pente douce du côté du vent et une
pente plus raide coté terre. La dune bordière délimite le haut de la plage par
un bourrelet sableux de un à quelques mètres de haut. Sa base correspond à la
haute mer et peut être endommagée lors des tempêtes. En arrière de ce premier
cordon, on peut voir se développer un champ de dunes montrant des formes
différentes : dunes alignées, dunes paraboliques, dunes en râteau, etc. plus ou
moins fixées par la végétation. Certains édifices anciens sont totalement végétalisés
et fixés alors que d’autres évoluent rapidement.</p>

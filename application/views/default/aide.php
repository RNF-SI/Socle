<h1><center>Aide / F.A.Q.</center></h1>

<h2>A qui s’adresse cette application ?</h2>
<p>L’application est un portail d’entrée vers la découverte du patrimoine géologique
des réserves naturelles et des espaces naturels protégés français en général.
Elle permet aux gestionnaires d’espaces protégés, géologues ou non de s’intéresser
au patrimoine géologique de leur territoire et de le présenter dans un cadre commun.</p>

<h2>Je souhaite saisir des données, comment m’inscrire ?</h2>
<p>L’inscription se fait à partir du site en cliquant sur « s’inscrire » en haut à droite de la page.
 Une fois les renseignements saisis, l’administrateur recevra un mail et pourra valider le compte.
 Il créera l’espace, enregistrera son périmètre géographique si il n’existe pas encore et donnera
 les autorisations pour saisir dans cet espace.</p>

<h2>Suis-je obligé de tout renseigner ?</h2>
<p>Aucune rubrique n’est obligatoire. Vous pouvez prendre le temps qu’il vous faut
pour remplir les formulaires à votre rythmes, les données sont toujours modifiables.</p>

<h2>Commencer la saisie des données</h2>
<p>Accédez à la fiche « de terrain » du site. Cette fiche détaille la marche à suivre
pour renseigner les données selon un ordre précis.
Elle est divisée en sous-rubriques accessibles par des panneaux déroulants.
Cliquez sur un titre pour faire apparaître les éléments renseignés. Cliquez sur « éditer » pour modifier les informations.
La plupart des informations à saisir se présente sous la forme de cases à cocher.</p>
<p>Une fois la saisie de la rubrique effectuée, cliquez sur « enregistrer »
pour sauvegarder les modifications.</p>

<h3>Visualisation et saisie des informations sur les entités géologiques</h3>
<p>Voir la définition ci-dessous</p>

<p>Les entités géologiques (EG) sont accessibles dans la rubrique 3.1 de la fiche de terrain du site.
A ce niveau, il est possible d’ajouter des entités géologiques.</p>
<p>Un formulaire permet de renter les caractéristiques générales de l’EG.
 Il faut tout d’abord pointer son emplacement approximatif sur la carte ;
 on peut marquer plusieurs points pour une même entité.
Le champs d’identification sont remplis automatiquement lorsque l’on clique sur la carte.
Ils sont modifiables par la suite.
Renseignez l’âge des roches à l’aide de l’échelle des temps géologique jusqu’au niveau le plus précis possible.
Cliquez sur « Enregistrer » pour sauvegarder l’EG.</p>
<p>Vous arrivez sur la fiche de l’entité. Celle-ci comporte des panneaux déroulants
de la même façon que pour la fiche du site, éditables.</p>

<h2>Terminologie utilisée sur le site</h2>
<p>L’exploration de la géologie du territoire nécessite des approches à plusieurs
  niveaux, du plus large au plus localisé.</p>


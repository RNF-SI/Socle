<p><strong>Espace naturel :</strong> L’ensemble du territoire géré par l’entité qui
  rentre les données : une réserve naturelle, un parc naturel régional…<p>

<p><strong>Site :</strong>subdivision de l’espace naturel sur laquelle porte l’étude de la géologie.
Le plus souvent (espace monosite), le site occupe l’intégralité du territoire de l’espace naturel.
Les deux niveaux sont alors confondus. Si le territoire est fractionné et/ou très hétérogène,
il peut être envisagé de délimitée plusieurs sites pour un même espace.</p>

<p><strong>Entité géologique :</strong> Correspond à une zone relativement homogène du point de vue géologique.
Dans la pratique, on considère qu’une entité géologique correspond à une classe
de légende sur la carte géologique au 1/50000 du BRGM.
Pour Socle, elle est représentée par un ou plusieurs points sur la carte.</p>

<p><strong>Objet remarquable :</strong> Sert à matérialiser des éléments ponctuels importants pour l’étude :
un affleurement, une formation rocheuse, un point de vue...</p>

<h3>Espace monosite ou multisite ?</h3>
<p>L’option la plus courante est monosite : un seul site identifié pour un espace naturel donné
  (sachant que l’on peut ensuite différencier des entités géologiques au sein du site).
  Cependant, si l’espace naturel est étendu et présente de fortes hétérogénéités géologiques
  ou s’il est éclaté géographiquement, on peut le subdiviser en sous-sites, et effectuer la saisie pour chacun d’eux.</p>
<p>Pour créer un espace multisite : se rendre sur la page de l’espace à partir de l’accueil
  (« Explorer » pour avoir la liste de tous les espaces), cliquer sur « modifier » et décocher
  la case « cet espace ne contient qu’un seul site ». Sur la page de l’espace, vous pourrez
  ensuite créer les sites que vous souhaitez.</p>
<p>Pour créer un site, il est nécessaire de lui donner un nom. On peut ensuite en cartographier le contour
  directement sur la carte grâce aux outils situés à gauche.</p>

<h2>Comment ajouter des illustrations / des documents de référence ?</h2>
<p>Vous pouvez ajouter des photos et documents au format PDF à deux niveaux :</p>
<ul>
<li>celui du site</li>
<li>celui de l’entité géologique</li>
</ul>
<p>Au niveau du site : les photos s’ajoutent via le formulaire de la partie 1.0 :
  cliquez sur « éditer » et téléchargez votre photo ou document, ajoutez une légende de description.</p>
<p>La première photo associée au site sert d’illustration de référence (apparaît en page d’accueil) :
   fournissez donc de préférence une photo présentant une vue d’ensemble du site.</p>

<h2>Je ne comprends pas certains termes</h2>
<p>Certains termes peuvent être inconnus des non géologues. Si le terme est accompagné d’un signe <sup>?</sup>,
vous pouvez passer la souris et une définition s’affichera. Vous pouvez aussi vous référer au
<a href="http://www.reserves-naturelles.org/sites/default/files/fichiers/dico_des_mots_de_geologie_screen.pdf">Dico des mots de la géologie</a>, disponible sur le site de RNF.</p>
